CREATE DATABASE IF NOT EXISTS note_db DEFAULT CHARSET UTF8;
GRANT ALL PRIVILEGES ON note_db.* TO note_db_user@'%' IDENTIFIED BY 'note_db_pass' WITH GRANT OPTION;

USE note_db;
CREATE TABLE IF NOT EXISTS `notes` (
  `id` VARCHAR(36),
  `title` VARCHAR(255) NOT NULL,
  `content` TEXT,
  `created_at` TIMESTAMP(3) DEFAULT CURRENT_TIMESTAMP(3),
  `updated_at` TIMESTAMP(3) DEFAULT CURRENT_TIMESTAMP(3),
  PRIMARY KEY USING BTREE (`id`)
);
